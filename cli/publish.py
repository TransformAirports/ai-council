"""Publish polished, distribution-ready reports from archived runs.

Discovery signal: every full-report .docx under `runs/*/stage4/` (executive
summaries are excluded). For each, the clean body is rebuilt from the matching
`stage3/final-draft.md` — the same source the stage4 report was built from, but
without that file's own cover/TOC/appendix, so the polished version doesn't end
up with duplicate front matter.

Each polished report gets, in order:
  1. A standard cover page (FINAL — FOR DISTRIBUTION).
  2. An Abstract page.
  3. A "How this report was produced" page — the four-stage Council process and
     the specific agents that were seated for this run (read from the run's
     stage1 brief filenames, which are ground truth for what actually ran).
  4. The body, with page numbers in the footer.
  5. An AI-generation disclaimer as the final section.

Output: `reports/<slug>.docx` at the repo root.
"""
from __future__ import annotations

import re
from dataclasses import dataclass
from datetime import date
from pathlib import Path

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml import OxmlElement
from docx.oxml.ns import qn
from docx.shared import Inches, Pt, RGBColor

from cli.agents import Agent, load_all_agents
from cli.docx_builder import _markdown_to_docx, _set_default_font, _strip_comments

REPO_ROOT = Path(__file__).resolve().parent.parent
RUNS_DIR = REPO_ROOT / "runs"
REPORTS_DIR = REPO_ROOT / "reports"
RUN_PROMPTS_DIR = REPO_ROOT / "prompts" / "runs"
ASSETS_DIR = REPO_ROOT / "assets"

# Cover logo. Drop a horizontal logo here to brand the cover; the build degrades
# gracefully (no logo) if the file is absent.
LOGO_CANDIDATES = [
    ASSETS_DIR / "council-logo.png",
    ASSETS_DIR / "logo.png",
]

# Typography. Georgia is a professional, print-grade serif that ships on both
# Windows and macOS, so the document renders the same on the recipient's
# machine. Headings carry the MWAA brand navy.
BODY_FONT = "Georgia"
HEADING_FONT = "Georgia"
BRAND_NAVY = RGBColor(0x14, 0x3C, 0x6E)
INK_SOFT = RGBColor(0x55, 0x55, 0x55)
DISTRIBUTION_GREEN = RGBColor(0x1A, 0x5A, 0x1A)

COVER_SUBHEADING = "An Analytical Report Generated by the AI Research Council"
ATTRIBUTION = "Transform Airports — AI Research Council"

AGENTS_DOC_URL = (
    "https://github.com/TransformAirports/ai-council/tree/main/.claude/agents"
)

ARCHIVE_DIR_PATTERN = re.compile(r"^\d{4}-\d{2}-\d{2}-(.+)$")
HEADING_RE = re.compile(r"^(#{1,6})\s+(.*)$")

PROCESS_AGENTS_NOTE = [
    ("Strategist", "synthesized the research briefs into a single argumentative draft."),
    ("Red Team", "attacked each draft to surface weak claims and logical gaps."),
    ("Editor", "tightened the prose and removed filler without adding new content."),
    ("Humanizer", "refined tone and readability without altering any claim, number, or citation."),
    ("Fact-checker", "verified every numerical and attributed claim against the briefs, after all editorial passes."),
]


def _find_logo() -> Path | None:
    for candidate in LOGO_CANDIDATES:
        if candidate.is_file():
            return candidate
    return None


def _apply_professional_styles(doc: Document) -> None:
    """Set the Normal and Heading styles to the professional font + brand color."""
    _set_default_font(doc, BODY_FONT, 11)
    for level, size in ((1, 17), (2, 14), (3, 12), (4, 11)):
        try:
            style = doc.styles[f"Heading {level}"]
        except KeyError:
            continue
        style.font.name = HEADING_FONT
        style.font.size = Pt(size)
        style.font.color.rgb = BRAND_NAVY
        style.font.bold = True


@dataclass
class ReportSource:
    slug: str
    archive_dir: Path
    stage4_docx: Path
    final_md: Path | None
    run_file: Path | None


# ----------------------------------------------------------------------------
# Discovery.
# ----------------------------------------------------------------------------

def discover_reports(runs_dir: Path = RUNS_DIR) -> list[ReportSource]:
    sources: list[ReportSource] = []
    if not runs_dir.is_dir():
        return sources
    for archive_dir in sorted(p for p in runs_dir.iterdir() if p.is_dir()):
        m = ARCHIVE_DIR_PATTERN.match(archive_dir.name)
        if not m:
            continue
        slug = m.group(1)
        stage4 = archive_dir / "stage4"
        if not stage4.is_dir():
            continue
        # Full report = the .docx that is NOT the executive summary.
        candidates = [
            p for p in stage4.glob("*.docx")
            if not p.stem.endswith("-executive-summary")
        ]
        if not candidates:
            continue
        stage4_docx = candidates[0]
        final_md = archive_dir / "stage3" / "final-draft.md"
        run_file = RUN_PROMPTS_DIR / f"{slug}.md"
        sources.append(
            ReportSource(
                slug=slug,
                archive_dir=archive_dir,
                stage4_docx=stage4_docx,
                final_md=final_md if final_md.is_file() else None,
                run_file=run_file if run_file.is_file() else None,
            )
        )
    return sources


# ----------------------------------------------------------------------------
# Metadata extraction.
# ----------------------------------------------------------------------------

def _section_from_run_file(text: str, header: str) -> str:
    lines = text.splitlines()
    target = f"## {header}"
    out: list[str] = []
    capturing = False
    for line in lines:
        if line.strip().startswith("## "):
            if line.strip() == target:
                capturing = True
                continue
            if capturing:
                break
        if capturing:
            out.append(line)
    return "\n".join(out).strip()


def _derive_title(source: ReportSource) -> str:
    if source.run_file is not None:
        text = source.run_file.read_text(encoding="utf-8", errors="ignore")
        for line in text.splitlines():
            if line.startswith("# Run:"):
                return line[len("# Run:"):].strip()
    # Fallback: title-case the slug.
    return source.slug.replace("-", " ").title()


def _derive_abstract(source: ReportSource) -> str:
    """Build a credible abstract without an LLM call.

    Preference order:
      1. The Thesis section of the run file (the document's central claim).
      2. The first substantive prose paragraph of the final draft.
    """
    if source.run_file is not None:
        text = source.run_file.read_text(encoding="utf-8", errors="ignore")
        thesis = _section_from_run_file(text, "Thesis")
        if thesis:
            scope = _section_from_run_file(text, "Success criteria")
            scope_line = ""
            if scope:
                items = [
                    re.sub(r"^[-*]\s*", "", ln).strip()
                    for ln in scope.splitlines()
                    if ln.strip().startswith(("-", "*"))
                ]
                if items:
                    scope_line = (
                        " The report examines this claim across the following: "
                        + "; ".join(s.rstrip(".") for s in items[:5])
                        + "."
                    )
            return thesis + scope_line

    if source.final_md is not None:
        text = _strip_comments(source.final_md.read_text(encoding="utf-8", errors="ignore"))
        paragraphs: list[str] = []
        buf: list[str] = []
        for line in text.splitlines():
            s = line.strip()
            if HEADING_RE.match(line) or not s:
                if buf:
                    paragraphs.append(" ".join(buf))
                    buf = []
                continue
            if s.startswith((">", "-", "*", "|")):
                continue
            buf.append(s)
        if buf:
            paragraphs.append(" ".join(buf))
        for p in paragraphs:
            if len(p.split()) >= 40:
                return p
        if paragraphs:
            return paragraphs[0]

    return "Abstract unavailable — source draft not found."


def _agents_used(source: ReportSource, agents_by_name: dict[str, Agent]) -> list[str]:
    """Display names of the research agents that actually ran this report.

    Ground truth = the brief filenames in stage1/.
    """
    stage1 = source.archive_dir / "stage1"
    used: list[str] = []
    if stage1.is_dir():
        for brief in sorted(stage1.glob("*-brief.md")):
            name = brief.name[: -len("-brief.md")]
            agent = agents_by_name.get(name)
            used.append(agent.display_name if agent else name.replace("-", " ").title())
    return used


